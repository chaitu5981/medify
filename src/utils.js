export const getTime = (date) => {
  const time = new Date(date).toLocaleTimeString("en-in", {
    hour: "2-digit",
    minute: "2-digit",
  });
  return time;
};

export const getDate = (date) => {
  const date1 = new Date(date).toLocaleDateString("en-in", {
    day: "numeric",
    month: "long",
    year: "numeric",
  });
  return date1;
};
